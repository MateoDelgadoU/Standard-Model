export const getParticleById = (id: string): Particle | undefined => {
  return particlesData.find((p) => p.id === id);
};

export const getParticlesByGeneration = (generation: number): Particle[] => {
  return particlesData.filter((p) => p.generation === generation);
};

export const getParticlesByType = (type: string): Particle[] => {
  return particlesData.filter((p) => p.type === type);
};

export const getParticlesByCategory = (category: string): Particle[] => {
  return particlesData.filter((p) => p.category === category);
};
